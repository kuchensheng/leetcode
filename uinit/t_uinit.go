package main

func main_() {
	/*
		uint 类型计算，转换成二进制再计算,以 8位 为例
		1 - 2 =  0 - 1
		也就是 0 + (-1) , 负数要转补码，-1的补码 [原]1 000 0001, [反] 1 111 1110,[补] 1111 1111
		因此 0 + ( -1) = 0000 0000 + 1111 1111 = 1111 1111 = 2 ^ 8 -1 = 255

	*/
	var a uint = 10
	var b uint = 18
	println(a - b)     // 2^64 - 1 = 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111
	println(a - b + 1) // 0
}
